// undefined
let numero;

console.log(numero); // undefined

// null
// "null' significa "dato desconocido" o "valor inexistente".

let num2 = null;

console.log(num2); // null

console.log(typeof num2);

console.log(numero == num2); // true

// Nota: siempre usar el comparador estricto
console.log(numero === num2); // false
